// while and do while loop



// while
let i = 1;

while (i <= 10) {
    // console.log(i);
    i++
}

let arr = ["flash", "batman", "superman"];
let j = 0;

while (j < arr.length) {
    // console.log(arr[j]);
    j++
}

// do while

let k = 1;

do {
    // console.log(k);
    k++

} while (k <= 10);